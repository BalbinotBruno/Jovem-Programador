
function validaTesouro(){
    let senhaCorreta = "ouro"

    let senha = document.getElementById('senha').validaTesouro

    while (senha) {
        if (senhaCorreta =! "senha") {
            console.log('Senha do Tesouro Incorreta')
        } else {
            console.log('Tesouro Liberado!')
        }
    }
}